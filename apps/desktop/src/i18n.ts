/** Translate a program description (PT comes from the catalog as the fallback). */
export function translateDesc(lang: Lang, id: string, fallbackPt: string): string {
  return lang === "en" ? DESC_EN[id] ?? fallbackPt : fallbackPt;
}

/** Translate a preset's [name, description] (PT from presets.json as fallback). */
export function translatePreset(
  lang: Lang,
  id: string,
  namePt: string,
  descPt: string
): [string, string] {
  return lang === "en" ? PRESET_EN[id] ?? [namePt, descPt] : [namePt, descPt];
}
